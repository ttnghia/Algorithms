#pragma once

#include <chrono>
#include <string>
#include <cassert>

#include "NumberUtils.h"
//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
using Clock = std::chrono::high_resolution_clock;

class Timer
{
public:
    Timer() : ticked(false)
    {}

    void tick()
    {
        assert(!ticked);
        start_time = Clock::now();
        ticked = true;
    }

    double tock()
    {
        assert(ticked);
        end_time = Clock::now();
        ticked = false;

        elapsed_time = std::chrono::duration<double, std::milli>(end_time - start_time).count();
        return elapsed_time;
    }

    std::string get_run_time()
    {
        if(ticked)
        {
            tock();
        }

        return NumberUtils::format_with_commas(elapsed_time) + "ms";
    }

private:
    Clock::time_point start_time;
    Clock::time_point end_time;
    double elapsed_time;

    bool ticked;
};
